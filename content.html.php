<h1> Gmail Compromise Checker </h1>

<p> In September 2014 nearly 5 Million Gmail Addresses were compromised. This site exists as a public service to allow users to
check their email address to see if they have been compromised. </p>

<p> If your email address is in this list then it is possible that it has been compromised. You should change your password 
immediately. If your email does not show up you should still enable <a href="https://www.google.com/landing/2step/"> Two Factor 
Authentication</a> to ensure that future attacks such as this one have minimal impact. </p>

<p> Please spread the word, and encourage everyone you know to use two factor authentication! </p>
